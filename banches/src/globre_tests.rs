    fn init() -> Self {
        Self {
            c: ExpressionBuilder::<BufferData>::new()
                .add(
                    "test",
                    Condition::try_new("value", Predicate::with_str_list(Operation::GlobREMatch, GLOB_PATTERNS, ValueKind::Path, true)),
                )
                .build("test")
                .unwrap(),
            obj: BufferData {
                value: String::with_capacity(512),
            },
        }
    }
    fn run_test(&mut self, count: usize) {
        for _ in 0..count {
            for value in PATHS {
                self.obj.value.clear();
                self.obj.value.push_str(value);
                assert!(self.c.try_matches(&self.obj).unwrap());
            }
        }
    }
    
    const NAME: &'static str = "GlobRE-match-1000";
    const DESCRIPTION: &'static str = "Match 1000 paths against 1000 glob patterns";
